let valorA = Number(prompt("Informe valor de A"))
let valorB = Number(prompt("Informe valor de B"))
let valorC = Number(prompt("Informe valor de C"))
if (valorA+valorB > valorC)
    alert("A soma dos valores A e B são maiores que o valor C")
else if (valorA+valorB < valorC)
    alert("A soma dos valores A e B são menores que o valor C")
else
    alert("A soma dos valores A e B é igual ao valor C")